// N. Numbers Histogram
# include <iostream>
#include <bits/stdc++.h>
using namespace std ;
int main ()
{

char s ;   //symbol
int lines;

cin>>s>>lines ;

for (int i=1 ;i<=lines ;i++)
{
    int num ;  //times 
    cin>>num ;
for(int j=1 ;j<=num ;j++)
{
    cout<<s;
}
cout <<endl;
}


return 0;

}








